def list_count_4(nums):
    count = nums.count(4)
    return count

print(list_count_4([1,3,5,3,6,6,4,4,4,4,6,7,"khldun"]))
print(list_count_4(['a', 123, 'hello', 'b', 456, 'world', 'c', 789, 'Python']))

